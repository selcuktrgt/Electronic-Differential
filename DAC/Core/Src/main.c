void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{

	pot_x=__HAL_TIM_GET_COUNTER(htim);
	if (2048<pot_x && pot_x<4096){

		count=-(4095-pot_x);
	}
	else{

		count=pot_x;
	}
}



void hesaplama() {

	if (count <= 0) // 4096==360 derece  Tam sol 0 , tam sağ 4096 düşünürsek
			{

		//sol ic teker
		a=(((-1)*count)*(2*pi/4096)/12);
		b=tan(a);
		d=(double)(pot_v*100)/4096*(1/0.5684);
		R=L/b;

		S0 = atan((L/(R - (w / 2))));
		S1 = atan((L/(R + (w / 2))));

		S0=atan(S0);
		S1=atan(S1);

		c=(sin((double)(S0)) - sin((double)(S1))) / (sin((double)(S0)) + sin((double)(S1)));

		K = (double)(c*d);

		Wsol =(double)(d - K);
		Wsag = (double)(d + K);



		Wsol = (4096 / 132) * Wsol;
		Wsag = (4096 / 146) * Wsag;


	}

	else if (count > 0) {

		//sag ic teker
		a=(count*(2*pi/4096)/12);
		b=tan(a);
		d=(double)(pot_v*100)/4096*(1/0.5684);
		R=L/b;

		S0 = atan((L/(R - (w / 2))));
		S1 = atan((L/(R + (w / 2))));

		S0=atan(S0);
		S1=atan(S1);

		c=(sin((double)(S0)) - sin((double)(S1))) / (sin((double)(S0)) + sin((double)(S1)));

		K = (double)(c*d);
		Wsol = (double)(d + K);
		Wsag = (double)(d - K);

		Wsol = (4096 / 146) * Wsol;
		Wsag = (4096 / 132) * Wsag;

	}
}


